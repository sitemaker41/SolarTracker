// موتور جستجوی هوشمند با اولویت‌بندی پیشرفته
// ============================================================
function findBestMatch(userQuestion) {
    // تمیز کردن متن
    let cleanText = userQuestion
        .replace(/[؟?،,!.;:]/g, '')
        .replace(/\s+/g, ' ')
        .trim()
        .toLowerCase();

    const stopWords = [
        'چیه', 'چیست', 'چی', 'یعنی', 'چطور', 'چجوری', 'لطفا', 'میشه',
        'بگو', 'بگید', 'گم', 'بدید', 'بفرمایید', 'خواهش', 'ببخشید',
        'یک', 'دو', 'سه', 'چهار', 'پنج', 'شش', 'هفت', 'هشت', 'نه', 'ده',
        'با', 'چه', 'که', 'این', 'آن', 'برای', 'از', 'به', 'رو', 'را', 'و', 'یا'
    ];

    let words = cleanText.split(' ');
    let filteredWords = words.filter(word => !stopWords.includes(word) && word.length > 1);
    let searchText = filteredWords.join(' ');

    let bestMatch = null;
    let bestScore = 0;

    // اولویت‌بندی موضوعات حیاتی
    const priorityTopics = [
        'نحوه کار پروژه', 'سلام و احوالپرسی', 'خداحافظی', 'تشکر',
        'اعضای تیم', 'قطعات پروژه', 'انرژی خورشیدی', 'پنل خورشیدی',
        'بازدهی', 'ردیاب خورشیدی دو محوره', 'ردیاب خورشیدی تک محوره',
        'سنسور LDR', 'آردوینو', 'موتور سروو', 'چاپ سه‌بعدی',
        'کلمات نامناسب', 'فحش'
    ];

    for (const item of solarBotData) {
        let score = 0;
        let isPriority = priorityTopics.includes(item.topic);

        // امتیاز برای کلمات کلیدی
        for (const keyword of item.keywords) {
            if (searchText.includes(keyword) || keyword.includes(searchText) ||
                cleanText.includes(keyword) || keyword.includes(cleanText)) {
                let weight = 1 + (keyword.length / 30);
                if (isPriority) weight *= 2.5;
                score += weight;
            }
        }

        // امتیاز ویژه برای تطابق کامل
        for (const keyword of item.keywords) {
            if (cleanText === keyword || searchText === keyword) {
                score += 15;
                if (isPriority) score += 10;
            }
        }

        if (score > bestScore) {
            bestScore = score;
            bestMatch = item;
        }
    }

    // اگر امتیاز خیلی کم بود، پاسخ عمومی بده
    if (bestScore < 0.5) {
        return {
            answer: `🤖 متوجه سوال شما نشدم. می‌توانید واضح‌تر بپرسید یا از کلمات کلیدی زیر استفاده کنید:

🔸 پروژه، نحوه کار، ردیاب
🔸 انرژی خورشیدی، پنل خورشیدی
🔸 ردیاب دو محوره، ردیاب تک محوره
🔸 سنسور LDR، آردوینو، موتور سروو
🔸 بازدهی، هزینه، قطعات
🔸 تیم، اعضا، سازندگان
🔸 سلام، خداحافظ، تشکر

اگر سوال خاصی دارید، با خیال راحت بپرسید. 😊`,
            score: bestScore,
            topic: "سوال نامشخص"
        };
    }

    return {
        answer: bestMatch.answer,
        score: bestScore,
        topic: bestMatch.topic
    };
}